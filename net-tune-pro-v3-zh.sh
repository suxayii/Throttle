# ===================== 校验/检查 =====================
extract_max_from_triplet(){ awk '{print $3}' <<<"$1"; }
escape_regex() { sed -e 's/[][(){}.^$*+?|\/]/\\&/g' <<<"$1"; }

validate_profile_semantics(){
  local tmp="$1"
  local rmem_max wmem_max tcp_rmem tcp_wmem tr tw
  rmem_max="$(awk -F= '/^[[:space:]]*net\.core\.rmem_max[[:space:]]*=/{gsub(/[ \t]/,"",$2);print $2}' "$tmp" | tail -n1)"
  wmem_max="$(awk -F= '/^[[:space:]]*net\.core\.wmem_max[[:space:]]*=/{gsub(/[ \t]/,"",$2);print $2}' "$tmp" | tail -n1)"
  tcp_rmem="$(awk -F= '/^[[:space:]]*net\.ipv4\.tcp_rmem[[:space:]]*=/{sub(/^[ \t]+/,"",$2);print $2}' "$tmp" | tail -n1)"
  tcp_wmem="$(awk -F= '/^[[:space:]]*net\.ipv4\.tcp_wmem[[:space:]]*=/{sub(/^[ \t]+/,"",$2);print $2}' "$tmp" | tail -n1)"
  tr="$(extract_max_from_triplet "$tcp_rmem")"
  tw="$(extract_max_from_triplet "$tcp_wmem")"
  [[ -n "$rmem_max" && -n "$tr" ]] && (( rmem_max < tr )) && { err "参数校验失败：rmem_max < tcp_rmem 最大值"; return 1; }
  [[ -n "$wmem_max" && -n "$tw" ]] && (( wmem_max < tw )) && { err "参数校验失败：wmem_max < tcp_wmem 最大值"; return 1; }
  
  # [NEW] 硬件限制检测与调整
  local total_mem_kb
  total_mem_kb="$(awk '/^MemTotal:/ {print $2}' /proc/meminfo 2>/dev/null || echo 0)"
  
  # 简单策略：单 Socket 最大缓冲区不应超过物理内存的 1/4 (避免 OOM)
  local max_allowed_kb=$(( total_mem_kb / 4 ))
  local rmem_max_kb=$(( rmem_max / 1024 ))
  local wmem_max_kb=$(( wmem_max / 1024 ))
  
  if (( total_mem_kb > 0 )); then
    if (( rmem_max_kb > max_allowed_kb )); then
        warn "检测到 rmem_max ($((rmem_max/1024/1024))MB) 超过系统建议值 ($((max_allowed_kb/1024))MB)"
        info "已自动调整 rmem_max 为安全值..."
        sed -i "s/^net.core.rmem_max.*/net.core.rmem_max = $((max_allowed_kb * 1024))/" "$tmp"
        # 同时尝试调整 tcp_* 最大值（如果预置方案包含）
        if grep -q "^net\.ipv4\.tcp_rmem" "$tmp"; then
            sed -i "s/^net.ipv4.tcp_rmem.*/net.ipv4.tcp_rmem = 4096 87380 $((max_allowed_kb * 1024))/" "$tmp"
        fi
    fi
    if (( wmem_max_kb > max_allowed_kb )); then
        warn "检测到 wmem_max ($((wmem_max/1024/1024))MB) 超过系统建议值 ($((max_allowed_kb/1024))MB)"
        info "已自动调整 wmem_max 为安全值..."
        sed -i "s/^net.core.wmem_max.*/net.core.wmem_max = $((max_allowed_kb * 1024))/" "$tmp"
        # 同时尝试调整 tcp_* 最大值（如果预置方案包含）
        if grep -q "^net\.ipv4\.tcp_wmem" "$tmp"; then
            sed -i "s/^net.ipv4.tcp_wmem.*/net.ipv4.tcp_wmem = 4096 65536 $((max_allowed_kb * 1024))/" "$tmp"
        fi
    fi
  fi

  return 0
}

precheck(){
  echo
  echo -e "${BOLD}【预检查阶段】${NC}"
  line
  check_base_deps || return 1

  # ===== 内核与拥塞控制状态 =====
  local kernel_ver current_cc current_qdisc
  kernel_ver="$(uname -r || true)"
  info "内核版本：$kernel_ver"
  
  if [[ -r /proc/sys/net/ipv4/tcp_available_congestion_control ]]; then
    local available_cc
    available_cc="$(cat /proc/sys/net/ipv4/tcp_available_congestion_control 2>/dev/null || true)"
    info "可用拥塞控制：$available_cc"
  fi
  
  # 当前拥塞控制状态
  current_cc="$(sysctl -n net.ipv4.tcp_congestion_control 2>/dev/null || echo unknown)"
  current_qdisc="$(sysctl -n net.core.default_qdisc 2>/dev/null || echo unknown)"
  info "当前拥塞控制：$current_cc"
  info "当前队列算法：$current_qdisc"

  # ===== 虚拟化类型检测 =====
  local virt_type
  virt_type=""
  if cmd_exists systemd-detect-virt; then
    virt_type="$(systemd-detect-virt 2>/dev/null || true)"
  elif [[ -r /sys/hypervisor/type ]]; then
    virt_type="$(cat /sys/hypervisor/type 2>/dev/null || true)"
  elif grep -qi "docker\|lxc" /proc/1/cgroup 2>/dev/null; then
    virt_type="container"
  fi
  
  if [[ -n "$virt_type" && "$virt_type" != "none" ]]; then
    case "$virt_type" in
      kvm)       info "虚拟化类型：KVM（支持自定义内核）" ;;
      qemu)      info "虚拟化类型：QEMU（支持自定义内核）" ;;
      xen)       info "虚拟化类型：Xen（支持自定义内核）" ;;
      vmware)    info "虚拟化类型：VMware（支持自定义内核）" ;;
      microsoft) info "虚拟化类型：Hyper-V（支持自定义内核）" ;;
      openvz)    warn "虚拟化类型：OpenVZ（⚠️ 无法更换内核）" ;;
      lxc|lxc-libvirt) warn "虚拟化类型：LXC 容器（⚠️ 无法更换内核）" ;;
      container|docker) warn "虚拟化类型：容器环境（⚠️ 无法更换内核）" ;;
      *) info "虚拟化类型：$virt_type" ;;
    esac
  else
    info "虚拟化类型：物理机或未检测到"
  fi

  # ===== CPU 性能检测 =====
  local cpu_model cpu_cores
  cpu_model="$(grep -m1 'model name' /proc/cpuinfo 2>/dev/null | cut -d: -f2 | xargs || echo unknown)"
  cpu_cores="$(nproc 2>/dev/null || echo 1)"
  info "CPU：$cpu_model（${cpu_cores}核）"
  
  # AES-NI 指令集检测（加密性能关键）
  if grep -qw aes /proc/cpuinfo 2>/dev/null; then
    ok "AES-NI：已支持（加密性能优秀）"
  else
    warn "AES-NI：未检测到（加密性能可能较低，建议使用 ChaCha20）"
  fi

  # ===== 网卡检测 =====
  local nic speed driver ethtool_speed bw_hint
  nic="$(ip route show default 2>/dev/null | awk '/default/ {print $5; exit}')"
  if [[ -n "${nic:-}" ]]; then
    info "默认网卡：$nic"
    
    # 获取驱动类型
    driver=""
    if [[ -r "/sys/class/net/$nic/device/driver" ]]; then
      driver="$(basename "$(readlink -f "/sys/class/net/$nic/device/driver")" 2>/dev/null || true)"
    elif [[ -r "/sys/class/net/$nic/device/uevent" ]]; then
      driver="$(awk -F= '/^DRIVER=/{print $2}' "/sys/class/net/$nic/device/uevent" 2>/dev/null || true)"
    fi
    
    # 尝试从 /sys 读取速度
    speed=""
    if [[ -r "/sys/class/net/$nic/speed" ]]; then
      speed="$(cat "/sys/class/net/$nic/speed" 2>/dev/null || true)"
    fi
    
    # 如果 /sys 返回 -1，尝试 ethtool
    ethtool_speed=""
    if [[ "$speed" == "-1" || -z "$speed" ]] && cmd_exists ethtool; then
      ethtool_speed="$(ethtool "$nic" 2>/dev/null | awk '/Speed:/ {print $2}' || true)"
      if [[ -n "$ethtool_speed" && "$ethtool_speed" != "Unknown!" ]]; then
        speed="$ethtool_speed"
      fi
    fi
    
    # 根据驱动类型推断带宽
    bw_hint=""
    case "${driver:-unknown}" in
      virtio_net|virtio-pci)
        bw_hint="10Gbps+（KVM/QEMU 虚拟化，实际取决于主机商限制）"
        ;;
      xen_netfront)
        bw_hint="取决于实例类型（Xen/AWS EC2 旧实例）"
        ;;
      ena)
        bw_hint="最高 100Gbps（AWS ENA 增强网络）"
        ;;
      hv_netvsc)
        bw_hint="取决于实例类型（Hyper-V/Azure）"
        ;;
      vmxnet3)
        bw_hint="10Gbps（VMware 虚拟化）"
        ;;
      veth)
        bw_hint="容器网络（Docker/LXC），带宽由主机决定"
        ;;
      *)
        bw_hint=""
        ;;
    esac
    
    # 输出检测结果
    if [[ -n "$driver" ]]; then
      info "网卡驱动：$driver"
    fi
    
    if [[ -n "$speed" && "$speed" != "-1" && "$speed" != "Unknown!" ]]; then
      info "网卡速率：${speed}Mbps"
    elif [[ -n "$bw_hint" ]]; then
      info "推测带宽上限：$bw_hint"
      echo -e "  ${YELLOW}💡 建议：选择「VPS 极致带宽版（虚拟网卡）」方案以最大化利用带宽${NC}"
    else
      info "网卡速率：虚拟网卡（无法检测物理链路速度）"
    fi
  fi

  # ===== UDP 端口检测 =====
  line
  info "正在检测 UDP 连通性..."
  local udp_test_result
  # 检查是否有 UDP 相关的 iptables 规则阻止
  if cmd_exists iptables; then
    local udp_drop_rules
    udp_drop_rules="$(iptables -L -n 2>/dev/null | grep -i "udp.*drop\|drop.*udp" | head -3 || true)"
    if [[ -n "$udp_drop_rules" ]]; then
      warn "检测到 UDP 丢弃规则（可能影响 Hysteria2 等 UDP 代理）："
      echo "$udp_drop_rules" | head -3 || true
    else
      ok "未检测到 UDP 阻止规则"
    fi
  fi

  # ===== 端口限速检测（tc qdisc） =====
  if cmd_exists tc && [[ -n "${nic:-}" ]]; then
    local tc_rules
    tc_rules="$(tc qdisc show dev "$nic" 2>/dev/null || true)"
    if echo "$tc_rules" | grep -qiE "tbf|htb|cake|police"; then
      warn "检测到流量整形规则（可能存在端口限速）："
      echo "$tc_rules" | grep -iE "tbf|htb|cake|police" | head -3 || true
    else
      ok "未检测到端口限速规则"
    fi
  fi

  # ===== 网络配置总览 =====
  line
  echo -e "${BOLD}=== 网卡信息 ===${NC}"
  ip link show 2>/dev/null | grep -E "^[0-9]+: " || true
  echo

  echo -e "${BOLD}=== TCP 拥塞控制 ===${NC}"
  sysctl net.ipv4.tcp_congestion_control 2>/dev/null || echo "  无法读取"
  echo

  echo -e "${BOLD}=== 默认队列调度器 ===${NC}"
  sysctl net.core.default_qdisc 2>/dev/null || echo "  无法读取"
  echo

  echo -e "${BOLD}=== TCP backlog 和最大连接数 ===${NC}"
  sysctl net.core.netdev_max_backlog net.core.somaxconn 2>/dev/null || true
  echo

  echo -e "${BOLD}=== TCP 缓冲区设置 ===${NC}"
  sysctl net.ipv4.tcp_rmem net.ipv4.tcp_wmem net.core.rmem_max net.core.wmem_max 2>/dev/null || true
  echo

  echo -e "${BOLD}=== 网卡队列长度 (txqueuelen) ===${NC}"
  local iface_dir
  for iface_dir in /sys/class/net/*; do
    local iface_name
    iface_name="$(basename "$iface_dir")"
    [[ "$iface_name" == "lo" ]] && continue
    echo -n "  $iface_name: "
    cat "$iface_dir/tx_queue_len" 2>/dev/null || echo "无法读取"
  done
  echo

  echo -e "${BOLD}=== BBR 模块加载情况 ===${NC}"
  if lsmod 2>/dev/null | grep -q bbr; then
    ok "BBR 已加载"
  else
    warn "BBR 未加载"
  fi
  echo

  echo -e "${BOLD}=== 适合代理建议 ===${NC}"
  echo -e "  ${GREEN}推荐配置: BBR + fq + txqueuelen>=8192 + netdev_max_backlog>=16384${NC}"
  echo "  查看上面各项是否满足判断即可"

  line
  [[ -w /etc/sysctl.d ]] || { err "/etc/sysctl.d 不可写"; return 1; }
  sysctl -a >/dev/null 2>&1 || { err "sysctl 读取失败"; return 1; }

  ok "预检查通过"
  return 0
}

conflict_check(){
  echo
  echo -e "${BOLD}【冲突检测】sysctl 重复键${NC}"
  line
  local files=()
  [[ -f /etc/sysctl.conf ]] && files+=(/etc/sysctl.conf)
  while IFS= read -r -d '' f; do files+=("$f"); done < <(find /etc/sysctl.d -maxdepth 1 -type f -name "*.conf" -print0 2>/dev/null | sort -z)
  [[ ${#files[@]} -eq 0 ]] && { warn "未找到配置文件"; return 0; }

  local out
  out="$(
    awk '
      function trim(s){gsub(/^[ \t]+|[ \t]+$/,"",s); return s}
      {
        line=$0; sub(/#.*/,"",line)
        if (line ~ /^[ \t]*$/ || line !~ /=/) next
        split(line,a,"="); key=trim(a[1]); if(key=="") next
        kf=key SUBSEP FILENAME
        if (!(kf in seen)) { seen[kf]=1; cnt[key]++; files[key]=files[key]?files[key] "," FILENAME:FILENAME }
      }
      END{ for(k in cnt) if(cnt[k]>1) print k " => " files[k] }
    ' "${files[@]}" | sort
  )"

  if [[ -z "$out" ]]; then
    ok "未发现重复键冲突"
  else
    warn "发现重复键："
    echo "$out"
    echo
    read -rp "是否尝试自动修复（注释掉非管理文件中的冗余项）？[y/N]: " yn
    if [[ "${yn:-N}" =~ ^[Yy]$ ]]; then
      resolve_conflicts "$out"
    fi
  fi
  line
}

resolve_conflicts(){
  local conflict_out="$1"
  [[ -z "$conflict_out" ]] && return 0

  local hdir
  hdir="$(save_last_and_history_snapshot)"
  info "修复前快照：$hdir"

  info "正在准备修复冲突..."
  local key file_list f
  while IFS= read -r row; do
    [[ -z "$row" ]] && continue
    key="${row%% => *}"
    file_list="${row#* => }"
    
    IFS=',' read -ra file_array <<< "$file_list"
    for f in "${file_array[@]}"; do
      # 排除脚本管理的文件
      if [[ "$f" == "$ACTIVE_FILE" ]]; then
        continue
      fi
      
      # 全量转义正则特殊字符
      local escaped_key
      escaped_key="$(escape_regex "$key")"

      if grep -qiE "^[[:space:]]*${escaped_key}[[:space:]]*=" "$f"; then
        if ! grep -q "commented by net-tune-pro repair" "$f"; then
          info "在 $f 中注释掉冲突键：$key"
          sed -i -E "s/^([[:space:]]*${escaped_key}[[:space:]]*=.*)$/# \1 # commented by net-tune-pro repair/" "$f"
        fi
      fi
    done
  done <<< "$conflict_out"
  
  ok "自动修复尝试完成。建议再次运行冲突检测。"
}

# ===================== 快照/回滚/永久基线 =====================
save_last_and_history_snapshot(){
  local stamp hdir
  stamp="$(ts)"
  rm -rf "${LAST_APPLY_DIR:?}/"* 2>/dev/null || true
  mkdir -p "$LAST_APPLY_DIR/sysctl.d"
  hdir="${HISTORY_DIR}/${stamp}"
  mkdir -p "$hdir/sysctl.d"

  [[ -f /etc/sysctl.conf ]] && cp -a /etc/sysctl.conf "$LAST_APPLY_DIR/sysctl.conf" || true
  [[ -f /etc/sysctl.conf ]] && cp -a /etc/sysctl.conf "$hdir/sysctl.conf" || true
  cp -a /etc/sysctl.d/*.conf "$LAST_APPLY_DIR/sysctl.d/" 2>/dev/null || true
  cp -a /etc/sysctl.d/*.conf "$hdir/sysctl.d/" 2>/dev/null || true

  [[ -f "$ACTIVE_FILE" ]] && cp -a "$ACTIVE_FILE" "$LAST_APPLY_DIR/managed-active.conf" || true
  [[ -f "$ACTIVE_FILE" ]] && cp -a "$ACTIVE_FILE" "$hdir/managed-active.conf" || true


  sysctl -a 2>/dev/null > "$LAST_APPLY_DIR/runtime-sysctl-all.txt" || true
  sysctl -a 2>/dev/null > "$hdir/runtime-sysctl-all.txt" || true
  echo "$hdir"
}

clean_old_snapshots(){
  local keep_count="${1:-20}"
  local snapshots count to_delete
  snapshots=($(ls -1d "${HISTORY_DIR}/"* 2>/dev/null | sort))
  count=${#snapshots[@]}
  
  if (( count <= keep_count )); then
    info "当前快照数量：$count（保留阈值：$keep_count），无需清理"
    return 0
  fi
  
  to_delete=$((count - keep_count))
  info "将清理 $to_delete 个旧快照，保留最近 $keep_count 个"
  
  for ((i=0; i<to_delete; i++)); do
    rm -rf "${snapshots[$i]}"
    info "已删除：${snapshots[$i]}"
  done
  
  ok "清理完成，当前剩余：$keep_count 个快照"
}

