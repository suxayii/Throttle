# ===================== 系统级深度优化（Virtio/GRO） =====================
apply_system_optimizations(){
  echo
  echo -e "${BOLD}【系统级深度优化（网卡/队列/中断）】${NC}"
  line

  local nic driver
  nic="$(ip route show default 2>/dev/null | awk '/default/ {print $5; exit}')"
  if [[ -z "${nic:-}" ]]; then
    err "未检测到默认网卡"
    return 1
  fi
  info "默认网卡：$nic"

  # 检测驱动类型
  driver=""
  if [[ -r "/sys/class/net/$nic/device/driver" ]]; then
    driver="$(basename "$(readlink -f "/sys/class/net/$nic/device/driver")" 2>/dev/null || true)"
  elif [[ -r "/sys/class/net/$nic/device/uevent" ]]; then
    driver="$(awk -F= '/^DRIVER=/{print $2}' "/sys/class/net/$nic/device/uevent" 2>/dev/null || true)"
  fi
  info "网卡驱动：${driver:-unknown}"

  if [[ "${driver:-}" != "virtio_net" && "${driver:-}" != "virtio-pci" ]]; then
    warn "当前网卡驱动不是 virtio_net，部分优化可能不适用"
    read -rp "是否继续？[y/N]: " yn
    if [[ ! "${yn:-N}" =~ ^[Yy]$ ]]; then
      return 0
    fi
  fi

  local cpu_count
  cpu_count="$(nproc 2>/dev/null || echo 1)"
  info "CPU 核心数：$cpu_count"

  # 自动安装 ethtool（多数优化依赖它）
  if ! cmd_exists ethtool; then
    warn "未检测到 ethtool，正在尝试自动安装..."
    if is_debian_like; then
      apt-get update -y >/dev/null 2>&1 || true
      apt-get install -y ethtool >/dev/null 2>&1 && ok "ethtool 安装成功" || warn "ethtool 安装失败，部分优化将跳过"
    elif cmd_exists yum; then
      yum install -y ethtool >/dev/null 2>&1 && ok "ethtool 安装成功" || warn "ethtool 安装失败，部分优化将跳过"
    else
      warn "无法自动安装 ethtool，请手动安装：apt install ethtool 或 yum install ethtool"
    fi
  fi

  line

  # ===== 1. Virtio 多队列 (Multiqueue) =====
  echo -e "${BOLD}[1/5] Virtio 多队列${NC}"
  if cmd_exists ethtool; then
    local max_combined current_combined
    max_combined="$(ethtool -l "$nic" 2>/dev/null | awk '/^Pre-set/,/^Current/{if(/Combined:/){print $2; exit}}')"
    current_combined="$(ethtool -l "$nic" 2>/dev/null | awk '/^Current/{flag=1} flag && /Combined:/{print $2; exit}')"
    if [[ -n "$max_combined" && "$max_combined" -gt 1 ]] 2>/dev/null; then
      local target=$((cpu_count < max_combined ? cpu_count : max_combined))
      if ethtool -L "$nic" combined "$target" 2>/dev/null; then
        ok "多队列已设置为 $target（最大 $max_combined）"
      else
        warn "多队列设置失败（当前：${current_combined:-unknown}）"
      fi
    else
      info "网卡不支持多队列或已是单队列"
    fi
  else
    warn "未安装 ethtool，跳过多队列设置"
  fi

  # ===== 2. RPS / RFS（软件级多核分发） =====
  echo -e "${BOLD}[2/5] RPS / RFS 多核分发${NC}"
  local rps_mask
  rps_mask="$(printf '%x' $(( (1 << cpu_count) - 1 )))"

  local rps_applied=0
  local queue_dir
  for queue_dir in /sys/class/net/"$nic"/queues/rx-*; do
    [[ -d "$queue_dir" ]] || continue
    if echo "$rps_mask" > "$queue_dir/rps_cpus" 2>/dev/null; then
      rps_applied=$((rps_applied + 1))
    fi
    if [[ -w "$queue_dir/rps_flow_cnt" ]]; then
      echo 4096 > "$queue_dir/rps_flow_cnt" 2>/dev/null || true
    fi
  done

  if [[ -w /proc/sys/net/core/rps_sock_flow_entries ]]; then
    echo 32768 > /proc/sys/net/core/rps_sock_flow_entries 2>/dev/null || true
  fi

  if (( rps_applied > 0 )); then
    ok "RPS 已应用到 $rps_applied 个接收队列（掩码：$rps_mask）"
    ok "RFS 已启用（flow_entries=32768，flow_cnt=4096）"
  else
    warn "RPS 设置失败（可能无权限）"
  fi

  # ===== 3. 网卡 Ring Buffer 加大 =====
  echo -e "${BOLD}[3/5] Ring Buffer${NC}"
  local max_rx="256" max_tx="256"
  if cmd_exists ethtool; then
    max_rx="$(ethtool -g "$nic" 2>/dev/null | awk '/^Pre-set/,/^Current/{if(/RX:/){print $2}}' | head -1 || true)"
    max_tx="$(ethtool -g "$nic" 2>/dev/null | awk '/^Pre-set/,/^Current/{if(/TX:/){print $2}}' | head -1 || true)"
    if [[ -n "$max_rx" && -n "$max_tx" ]]; then
      if ethtool -G "$nic" rx "$max_rx" tx "$max_tx" 2>/dev/null; then
        ok "Ring Buffer 已设为最大值（RX=$max_rx TX=$max_tx）"
      else
        info "Ring Buffer 设置失败或已是最大值"
      fi
    else
      info "Ring Buffer 信息读取失败，跳过"
    fi
  else
    warn "未安装 ethtool，跳过 Ring Buffer 设置"
  fi

  # ===== 4. 硬件卸载（GSO/GRO/TSO） =====
  echo -e "${BOLD}[4/5] 硬件卸载检查${NC}"
  if cmd_exists ethtool; then
    for feat in gso gro tso; do
      # ethtool -k 输出的是长名称，需要映射
      local long_feat=""
      case "$feat" in
        gso) long_feat="generic-segmentation-offload" ;;
        gro) long_feat="generic-receive-offload" ;;
        tso) long_feat="tcp-segmentation-offload" ;;
      esac

      local status
      # 使用 awk 提取状态，避免 grep 找不到时返回 1 导致脚本退出 (set -e)
      status="$(ethtool -k "$nic" 2>/dev/null | awk -v f="$long_feat" '$1 == f":" {print $2}')"
      
      if [[ "$status" == "off" ]]; then
        if ethtool -K "$nic" "$feat" on 2>/dev/null; then
          ok "$feat 已开启"
        else
          warn "$feat 开启失败（可能不支持）"
        fi
      else
        ok "$feat 已是开启状态"
      fi
    done
  else
    warn "未安装 ethtool，跳过硬件卸载检查"
  fi

  # ===== 5. 中断合并（减少 CPU 开销） =====
  echo -e "${BOLD}[5/5] 中断合并${NC}"
  if cmd_exists ethtool; then
    if ethtool -C "$nic" adaptive-rx on adaptive-tx on 2>/dev/null; then
      ok "自适应中断合并已开启"
    else
      info "自适应中断合并设置失败（virtio 可能不支持，这是正常的）"
    fi
  else
    warn "未安装 ethtool，跳过中断合并设置"
  fi

  line
  ok "系统级深度优化完成"

  if [[ "${1:-}" == "persist" ]]; then
    setup_persistence "$nic" "$cpu_count" "$rps_mask" "$max_rx" "$max_tx"
  else
    warn "当前为临时生效模式，重启后将失效"
  fi
}

set_txqueuelen(){
  echo
  echo -e "${BOLD}【设置网卡发送队列长度 (txqueuelen)】${NC}"
  line

  local nic current_len
  nic="$(ip route show default 2>/dev/null | awk '/default/ {print $5; exit}')"
  [[ -z "$nic" ]] && nic="eth0"
  
  current_len="$(ip link show "$nic" 2>/dev/null | grep -oP 'txqueuelen \K\d+' || true)"
  info "当前网卡：$nic"
  info "当前队列长度：${current_len:-未知}"

  local new_len
  read -rp "请输入新的 txqueuelen 数值 (建议 5000-20000): " new_len
  if [[ ! "$new_len" =~ ^[0-9]+$ ]]; then
    err "输入无效，必须为数字"
    return 1
  fi

  if ip link set "$nic" txqueuelen "$new_len"; then
    ok "已成功设置 $nic 的 txqueuelen 为 $new_len"
  else
    err "修改失败，请检查权限或网卡名称"
    return 1
  fi

  read -rp "是否将此设置添加到开机自启 (rc.local)？[y/N]: " yn
  if [[ "${yn:-N}" =~ ^[Yy]$ ]]; then
    local rc_file="/etc/rc.local"
    # 确保 rc.local 存在
    if [[ ! -f "$rc_file" ]]; then
      printf '#!/bin/bash\nexit 0\n' > "$rc_file"
      chmod +x "$rc_file"
    fi
    # 移除旧的 txqueuelen 设置（如果有）
    sed -i "/ip link set .* txqueuelen/d" "$rc_file"
    # 在 exit 0 之前插入
    sed -i "/exit 0/i ip link set $nic txqueuelen $new_len" "$rc_file"
    ok "已添加到 /etc/rc.local"
  fi
}

setup_persistence(){
  local nic="$1" cpu_count="$2" rps_mask="$3" max_rx="${4:-256}" max_tx="${5:-256}"
  local rc_file="/etc/rc.local"
  
  info "正在配置开机自启..."

  # 确保 rc.local 存在并可执行
  if [[ ! -f "$rc_file" ]]; then
    printf '#!/bin/bash\nexit 0\n' > "$rc_file"
  fi
  chmod +x "$rc_file"

  # 清理旧的优化命令
  sed -i '/# Net-Tune-Pro-System-Optimize-Start/,/# Net-Tune-Pro-System-Optimize-End/d' "$rc_file"
  sed -i '/exit 0/d' "$rc_file"

  # 生成优化脚本片段
  local opt_block
  opt_block="# Net-Tune-Pro-System-Optimize-Start
# Generated by net-tune-pro at $(date)
nic=\"$nic\"
if [[ -d /sys/class/net/\$nic ]]; then
  # Multiqueue
  ethtool -L \$nic combined $cpu_count 2>/dev/null
  # Ring Buffer
  ethtool -G \$nic rx $max_rx tx $max_tx 2>/dev/null
  # Offload
  ethtool -K \$nic gso on gro on tso on 2>/dev/null
  # RPS
  for q in /sys/class/net/\$nic/queues/rx-*; do
    echo $rps_mask > \$q/rps_cpus 2>/dev/null
    echo 4096 > \$q/rps_flow_cnt 2>/dev/null
  done
  # RFS
  echo 32768 > /proc/sys/net/core/rps_sock_flow_entries 2>/dev/null

fi
# Net-Tune-Pro-System-Optimize-End"

  echo "$opt_block" >> "$rc_file"
  echo "exit 0" >> "$rc_file"
  
  ok "已添加到 /etc/rc.local，开机将自动应用优化"
  
  # 检查 rc-local 服务状态
  if systemctl is-active rc-local >/dev/null 2>&1 || systemctl is-enabled rc-local >/dev/null 2>&1; then
    :
  else
    warn "检测到 rc-local 服务未启用，尝试启用..."
    systemctl enable rc-local >/dev/null 2>&1 || true
    systemctl start rc-local >/dev/null 2>&1 || true
  fi
}

