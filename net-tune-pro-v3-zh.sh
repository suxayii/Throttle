  echo
  ok "已清除所有限速/整形规则，并重置为 fq。"
  echo "注意：如果是 Throttle.sh 残留的 limit，现在已被移除。"
}

view_current_rules(){
  echo
  echo -e "${BOLD}【当前优化规则校验】${NC}"
  line
  
  # 获取并显示当前 Profile 名称
  local p_code p_desc
  if [[ -f "$PROFILE_STATE_FILE" ]]; then
    p_code="$(cat "$PROFILE_STATE_FILE" | tr -d ' \n\r')"
    p_desc="$(get_profile_name "$p_code")"
    echo "当前应用方案：${GREEN}${p_desc}${NC}"
  else
    echo "当前应用方案：${YELLOW}未知 (未检测到状态文件)${NC}"
  fi

  local config_file="$ACTIVE_FILE"
  if [[ ! -f "$config_file" ]]; then
    warn "未找到托管配置文件：$config_file"
    read -rp "是否查看 /etc/sysctl.conf ? [y/N]: " yn
    if [[ "${yn:-N}" =~ ^[Yy]$ ]]; then
       config_file="/etc/sysctl.conf"
    else
       return
    fi
  fi

  echo "配置文件路径：$config_file"
  printf "${BOLD}%-32s %-20s %-20s %s${NC}\n" "参数键 (Key)" "配置 (Conf)" "系统 (Sys)" "状态"
  line

  # 使用 awk 预处理：去重（最后写入生效）、去除注释空行
  # output format: key=value
  local effective_conf awk_src
  # Use read to load awk script into variable safe from quoting issues
  # || true is needed because read returns 1 when it hits EOF without delimiter
  read -r -d '' awk_src <<'AWK_EOF' || true
    BEGIN { FS="=" }
    /^[[:space:]]*#/ { next }
    /^[[:space:]]*$/ { next }
    {
      # Normalize key: trim spaces
      key=$1; gsub(/^[ \t]+|[ \t]+$/, "", key)
      
      # Normalize val: trim spaces, remove inline comments
      val=$2; sub(/#.*/, "", val); gsub(/^[ \t]+|[ \t]+$/, "", val)
      
      if (key != "" && val != "") {
        conf[key] = val
        if (!(key in seen)) {
            keys[++k] = key
            seen[key] = 1
        }
      }
    }
    END {
      for (i=1; i<=k; i++){
        print keys[i] "=" conf[keys[i]]
      }
    }
AWK_EOF
  effective_conf="$(awk "$awk_src" "$config_file")"

  while IFS='=' read -r key val; do
    # Get system value
    local sys_val
    sys_val="$(sysctl -n "$key" 2>/dev/null || echo "MISSING")"
    
    # Normalize for comparison: collapse multiple spaces/tabs into single space
    local clean_val clean_sys
    clean_val="$(echo "$val" | tr '\t' ' ' | tr -s ' ' | sed 's/^[[:space:]]*//;s/[[:space:]]*$//')"
    clean_sys="$(echo "$sys_val" | tr '\t' ' ' | tr -s ' ' | sed 's/^[[:space:]]*//;s/[[:space:]]*$//')"

    local status
    if [[ "$sys_val" == "MISSING" ]]; then
        status="${RED}MISSING${NC}"
    elif [[ "$clean_val" == "$clean_sys" ]]; then
        status="${GREEN}OK${NC}"
    else
        status="${YELLOW}DIFF${NC}"
    fi
    
    # Truncate values for display if too long
    local d_val="$val"
    local d_sys="$sys_val"
    if (( ${#d_val} > 20 )); then d_val="${d_val:0:18}.."; fi
    if (( ${#d_sys} > 20 )); then d_sys="${d_sys:0:18}.."; fi
    
    printf "%-32s %-20s %-20s %b\n" "$key" "$d_val" "$d_sys" "$status"

  done <<< "$effective_conf"
  line
  echo "注：DIFF 表示系统实际运行值与配置文件不一致 (忽略空白符差异)"
  pause
}

# ===================== 脚本更新 =====================
SCRIPT_PATH="/root/net-tune-pro-v3-zh.sh"

update_script(){
  echo
  echo -e "${BOLD}【脚本更新】${NC}"
  line
  info "当前版本：$VERSION"
  info "正在检查新版本..."

  local update_url="https://raw.githubusercontent.com/suxayii/Throttle/master/net-tune-pro-v3-zh.sh"
  local tmp_file="/tmp/net-tune-pro-update.sh"

  if cmd_exists curl; then
    curl -fsSL "$update_url" -o "$tmp_file"
  elif cmd_exists wget; then
    wget -qO "$tmp_file" "$update_url"
  else
    err "未找到 curl 或 wget，无法下载更新"
    return 1
  fi

  if [[ ! -s "$tmp_file" ]]; then
    err "下载失败或文件为空，请检查网络连接"
    rm -f "$tmp_file"
    return 1
  fi

  # 简单的内容比对
  if [[ -f "$SCRIPT_PATH" ]] && cmp -s "$SCRIPT_PATH" "$tmp_file"; then
    ok "当前已是最新版本"
    rm -f "$tmp_file"
    return 0
  else
    # 尝试提取新版本号（如果脚本里有定义 VERSION）
    local new_ver
    new_ver="$(grep '^VERSION=' "$tmp_file" | head -1 | cut -d'"' -f2 || true)"
    [[ -z "$new_ver" ]] && new_ver="未知"
    
    info "发现新版本：$new_ver"
    read -rp "是否更新脚本？[y/N]: " yn
    if [[ "${yn:-N}" =~ ^[Yy]$ ]]; then
      install -m 0755 "$tmp_file" "$SCRIPT_PATH"
      rm -f "$tmp_file"
      ok "脚本已更新到 $SCRIPT_PATH，即将重新加载..."
      sleep 2
      exec bash "$SCRIPT_PATH"
    else
      info "已取消更新"
      rm -f "$tmp_file"
    fi
  fi
}

