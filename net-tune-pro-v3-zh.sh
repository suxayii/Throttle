is_debian_like(){
  cmd_exists apt-get
}

# ===================== 依赖检查（尽量不强装） =====================
check_base_deps(){
  local miss=0
  for c in sysctl awk grep sed ip ss find mktemp install; do
    if ! cmd_exists "$c"; then
      err "缺少基础依赖：$c"
      miss=1
    fi
  done
  if (( miss == 1 )); then
    err "基础依赖不完整，请先安装后重试。"
    return 1
  fi
  return 0
}



ensure_mtr(){
  if cmd_exists mtr; then
    return 0
  fi

  info "未检测到 mtr，正在尝试自动安装..."
  if is_debian_like; then
    apt-get update -y >/dev/null 2>&1 || true
    if apt-get install -y mtr-tiny >/dev/null 2>&1 || apt-get install -y mtr >/dev/null 2>&1; then
      ok "mtr 安装成功"
      return 0
    fi
  elif cmd_exists yum; then
    if yum install -y mtr >/dev/null 2>&1; then
      ok "mtr 安装成功"
      return 0
    fi
  elif cmd_exists dnf; then
    if dnf install -y mtr >/dev/null 2>&1; then
      ok "mtr 安装成功"
      return 0
    fi
  fi

  err "mtr 自动安装失败，请手动安装后重试。"
  return 1
}

